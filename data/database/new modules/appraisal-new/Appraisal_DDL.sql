CREATE TABLE "HRIS_APPRAISAL_TYPE"
  (
    "APPRAISAL_TYPE_ID"   NUMBER(7,0),
    "APPRAISAL_TYPE_NAME" VARCHAR2(100 BYTE),
    DURATION_TYPE         CHAR(1 BYTE) DEFAULT 'A' NOT NULL CHECK(DURATION_TYPE IN ('A','M')),
    "CREATED_BY"          NUMBER(7,0),
    "CREATED_DATE"        DATE DEFAULT SYSDATE,
    "DELETED_BY"          NUMBER(7,0),
    "DELETED_DATE"        DATE,
    "STATUS"              CHAR(1 BYTE),
    "REMARKS"             VARCHAR2(255 BYTE)
  ) ;
