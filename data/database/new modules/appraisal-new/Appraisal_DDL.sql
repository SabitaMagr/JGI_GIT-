  (
    "STAGE_ID"   NUMBER(7,0),
    "STAGE_NAME" VARCHAR2(100 BYTE),
    "STAGE_FOR"  VARCHAR2(255 BYTE) CHECK(STAGE_FOR IN ('EMPLOYEE','RECOMMENDER','APPROVER','SUPER_REVIEWER','HR')) "SEND_NOTIFICATION" CHAR(1 BYTE) NOT NULL,
